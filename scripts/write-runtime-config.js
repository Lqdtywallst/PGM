#!/usr/bin/env node

const fs = require('node:fs');
const path = require('node:path');

const repoRoot = path.resolve(__dirname, '..');
const outputPath = path.join(repoRoot, 'site', 'runtime-config.js');

function readPublicEnv(...names) {
    for (const name of names) {
        const value = process.env[name];

        if (typeof value === 'string' && value.trim()) {
            return value.trim();
        }
    }

    return '';
}

function normalizeEnvironment(value) {
    const normalized = String(value || '').trim().toLowerCase();

    if (['development', 'dev', 'local'].includes(normalized)) {
        return 'development';
    }

    if (['staging', 'stage', 'preview', 'preprod', 'preproduction'].includes(normalized)) {
        return 'staging';
    }

    if (['production', 'prod'].includes(normalized)) {
        return 'production';
    }

    return '';
}

function isSafePublishableKey(value) {
    return /^pk_(test|live)_[A-Za-z0-9_]+$/.test(String(value || '').trim());
}

function buildRuntimeConfig() {
    const appEnv = normalizeEnvironment(readPublicEnv('PGM_APP_ENV', 'APP_ENV'));
    const backendUrl = readPublicEnv('PGM_PUBLIC_BACKEND_URL', 'PUBLIC_BACKEND_URL');
    const publishableKey = readPublicEnv('PGM_PUBLIC_STRIPE_PUBLISHABLE_KEY', 'PUBLIC_STRIPE_PUBLISHABLE_KEY', 'STRIPE_PUBLISHABLE_KEY');
    const runtimeConfig = {};

    if (appEnv) {
        runtimeConfig.appEnv = appEnv;
    }

    if (backendUrl) {
        runtimeConfig.backendUrl = backendUrl.replace(/\/+$/, '');
    }

    if (publishableKey) {
        if (!isSafePublishableKey(publishableKey)) {
            throw new Error('Refusing to write Stripe key. Use a public pk_test_/pk_live_ publishable key only.');
        }

        runtimeConfig.publishableKey = publishableKey;
    }

    return runtimeConfig;
}

function renderRuntimeConfig(runtimeConfig) {
    const serializedConfig = JSON.stringify(runtimeConfig, null, 4);

    return `// Generated by scripts/write-runtime-config.js during deployment.
// Safe public runtime config for the static frontend. Secrets must never be written here.
(function () {
    const runtimeConfig = ${serializedConfig};

    window.PGM_RUNTIME_CONFIG = Object.assign({}, window.PGM_RUNTIME_CONFIG || {}, runtimeConfig);

    if (runtimeConfig.appEnv) {
        window.__APP_ENV__ = runtimeConfig.appEnv;
    }
}());
`;
}

const runtimeConfig = buildRuntimeConfig();
fs.writeFileSync(outputPath, renderRuntimeConfig(runtimeConfig), 'utf8');

const summary = {
    appEnv: runtimeConfig.appEnv || '(auto)',
    backendUrl: runtimeConfig.backendUrl || '(config.js default)',
    publishableKey: runtimeConfig.publishableKey ? `${runtimeConfig.publishableKey.slice(0, 8)}...` : '(config.js default)'
};

console.log(`[runtime-config] wrote ${path.relative(repoRoot, outputPath)} ${JSON.stringify(summary)}`);
